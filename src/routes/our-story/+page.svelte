        <h1>How You & Me Became We</h1>
    </header>
    <p>
        They say the most precious gems are found in the most unexpected places. For Deep and Kelly, their greatest treasure wasn't discovered in a moment of grandeur, but in the gentle unfolding of an ordinary evening – a dinner invitation that would change their lives forever.
    </p>

    <p>
        In the quaint college town of Davis, California, destiny was quietly at work, weaving together threads of chance that would create an unbreakable tapestry of love. It began with Leander, a foreign exchange student who, by divine orchestration, became Deep's last-minute housemate. In what could only be described as heaven's choreography, Leander knew Kelly from her work at the Global Studies Program, and by another whisper of fate, she lived in the very same apartment complex.
    </p>

    <p>
        One evening, the stars aligned. After spotting Kelly studying in the apartment lounge, Leander orchestrated a simple dinner gathering. Deep, exhausted from his lab work, reluctantly dragged his feet to what he thought would be just another meal. Little did he know, he was walking toward the first chapter of his forever story.
    </p>

    <p>
        As seasons changed, their friendship blossomed in the garden of ordinary moments – laughter echoing through game nights, shared glances across restaurant tables in downtown Davis, and movie marathons that felt like time had stopped just for them. Deep and Kelly's connection sparked like electricity, their shared humor creating a dance of joy that made everyone around them smile.
    </p>

    <p>
        Then came the night that would be etched in Deep's heart forever. Walking back from Jack in the Box under a canopy of stars, when the world seemed to hold its breath, Deep saw Kelly as if for the first time. As she spoke passionately (about basketball, though Deep swears that wasn't what captured his heart), her brown hair danced in the midnight breeze, and her eyes – oh, her eyes – held all the light of the universe within them. In that moment, though he tried to deny it, his heart had already begun to write their story.
    </p>

    <p>
        But timing is everything in love's grand design. When Deep finally gathered his courage, it came wrapped in the most endearing disguise – an invitation for hot chocolate because he had "made too much." What began at 9 PM in the apartment lounge transformed into a conversation that refused to end until 11 AM the next day, as if time itself couldn't bear to interrupt their connection. That night, Deep whispered words to Leander that seemed to echo with prophecy: "I don't know if we'll ever date, but if we do... watch. We'll get married."
    </p>

    <p>
        Their bond deepened like roots searching for water, through countless nights of soul-bearing conversations and moments of pure understanding. When Kelly confessed her growing feelings, Deep's heart soared – but both knew that the strongest foundations are built with patience. Kelly embarked on a year of personal growth and spiritual discovery, while Deep nurtured his own journey of becoming the man he was meant to be.
    </p>

    <p>
        During this time of waiting, their love grew in the spaces between moments – creating an entire SNL episode together, serving their community hand in hand, celebrating at UC Davis games (becoming legendary for their T-shirt-catching abilities), and building a tapestry of shared adventures. Their friends watched in wonder as what was clearly written in the stars waited to bloom on earth.
    </p>    

    <p>
        Even when the pandemic forced them apart, Deep's love only grew stronger. His words to her still echo: "Being in your life is already the greatest gift. Anything beyond friendship is just more chances to love and know you – and that would be my life's greatest joy." In those words lay the purest truth of their love – a love that asked for nothing but the chance to give everything.
    </p>

    <p>
        When January 4, 2021 finally arrived, their patience bloomed into partnership. These past three years have painted a masterpiece of cultural harmony – Deep's rich Indian heritage, with its emphasis on family, hard work, and warm hospitality, intertwining perfectly with Kelly's Western upbringing, which brought individual celebration, joy, and the art of nurturing boundaries while lifting each other higher.
    </p>

    <p>
        Their love story transcends romance – it's a healing river flowing through generations. They see their union as sacred ground where two family trees, once fractured, begin to heal and grow together. Years from now, their descendants will point to this marriage as the moment when love began to repair what was broken – when everything started to go right.
    </p>

    <p>
        Together, Deep and Kelly radiate a love that spills over into service, generosity, and pure joy. Their hearts beat as one with their faith, and they stand ready to build a life anchored in service, their marriage the foundation of all the beauty that is yet to come.
    </p>

    <p>
        Theirs isn't just a love story – it's a testament to divine timing, patient hearts, and the miracle that happens when two souls choose to grow together while honoring their truest selves. As they prepare to join their lives in marriage, they know with unshakeable certainty that this is just the beginning of their greatest adventure.
    </p>

